package jt.loop;

public class ForHandler {
    public static void main(String[] args) {

        System.out.println("This is first for loop");
        for (int i = 0; i < 10; i++) {
            System.out.print(i + ",");
        }


        System.out.println("\n" + "This is second for loop");
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
    }
}
